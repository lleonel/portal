				    	<figure class="post-icon"><a href="#" title=""><img src="img/common/1855_raw.jpg" alt=""/></a></figure>
				    	<h1><span id="ctl00_Contenido_lbl_Titulo">El deporte social es una política de Estado. Dialogos con Alicia Kirchner</span><a href="#" title=""></a></h1>
				    	<time datetime="2013-01-09T15:19:59+00:00" class="entry-date"><span id="ctl00_Contenido_lbl_fecha">15 de octubre de 2013</span></time>
				    	<p>La Ministra de Desarrollo Social comparte su opinión sobre la importancia del deporte social, el apoyo a los clubes de barrio y la participación de niños y niñas en los Juegos Evita.</p>
				    	<p>Que actividades se desarrollan en el marco del deporte social?</p>
				    	<p>Son múltiples las actividades que se desarrollan dentro del deporte social. De hecho, en los Centros Integradores Comunitarios tenemos muchos espacios para que se incentive el deporte. Por otro lado, hay una política de apoyo muy fuerte hacia todos los clubes de barrio. El deporte social es una política pública y de Estado.</p>
				    	<p>Cómo se instrumenta esta política en el territorio?</p>
				    	<p>Nosotros elegimos, por ejemplo, en el conurbano, las zonas más vulnerables donde resultaba prioritaria nuestra llegada. Nuestro objetivo fue y es que los chicos puedan realizar actividades deportivas, lo cual es muy bueno para su salud y para su desarrollo. Por eso, fue decisión de la presidenta empezar a hacer centros de extensión escolar y educación física para que el alumno pueda prorroga su jornada escolar. A su vez, los chicos reciben un apoyo alimentario especial y pueden realizar actividades deportivas de extensión. Este es el primer paso, el primer escalón para seguir desarrollando el deporte social.</p>
				    	<p>En qué consisten las jornadas de extensión escolar y centros de educación física?</p>
				    	<p>Hace poco, tanto en Jose C. Paz como en La Matanza, se comenzó a implementar el programa de extensión escolar y centros de educación física para que más de 40 mil niños puedan incluirse en actividades de extensión escolar que a través del deporte les permite una mayor inclusión social. Además, estamos haciendo con distintos deportistas del país, escuelas de deportes que tienen el objetivo de lograr la inclusión social. Y tenemos los Juegos Evita, que tienen toda una historia dentro de lo que es el movimiento del justicialismo, pero que recién a partir del 2004 empezaron a consolidarse anualmente dentro del calendario deportivo. Este año, aspiramos tener una participación de 1 millón 200 mil niños. Movilizar 1 millón 200 mil niños y niñas de todo el país es toda una actividad de integración, una actividad social más allá del deporte en si mismo. Nosotros decimos que en el deporte todos ganan.</p>
				    	<p>Un punto interesante para destacar es el trabajo que se genera en torno a los Juegos Evita para producir los insumos.</p>
				    	<p>Exactamente. Incentivamos y promovemos el desarrollo de emprendimientos. Muchísimos talleres textiles o cooperativas aportan su equipamiento, también hay emprendimientos que aportan las pelotas. Entonces, todo se integra, hay una suerte de articulación.</p>
				    	<p>Que nos puede contar sobre los programas que apoyan a los clubes barriales?</p>
				    	<p>Tenemos los clubes que están totalmente consolidados que son apoyados a lo largo y a lo ancho de todo el país, y tenemos los clubes de barrio que dan sus primeros pasos, a los cuales, por supuesto, también apoyamos. Hay zonas donde tenemos un desarrollo de gimnasios, de clubes de barrio muy importantes y hay otras zonas que no tienen nada. Los que trabajamos en políticas públicas de desarrollo social tenemos que trabajar mirando y escuchando en el territorio. El Estado tiene que volver a equilibrar todo y eso es lo que estamos haciendo.</p>
				    	<p>(*) Esta entrevista fue realizada en julio de 2011.</p>
